// Variables are names that point to a value

// Defining variables

// var
var a; // declares a variable called a
a = 50; // equal sign is used to assign a value to a variable
// var isn't used anymore these days

// let
let b = 100; // can be reassigned
b = 300;

// const
const c = 150; // value must be assigned right away

// Use const whenever possible
// You can do multiple assignments in one line

const name = 'Daenerys', age = 29;


